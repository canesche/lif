#include "../include/twofish.h"
#include <stdio.h>
#include <stdlib.h>

int main() {
    uint8_t in_key[16] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77,
                          0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF};
    uint8_t in[16] = {0x83, 0xA2, 0xF8, 0xA2, 0x88, 0x64, 0x1F, 0xB9,
                      0xA4, 0xE9, 0xA5, 0xCC, 0x2F, 0x13, 0x1C, 0x7D};
    uint8_t out[16] = {0};
    TWOFISH_context ctx;
    do_twofish_setkey(in_key, &ctx, 16);
    do_twofish_encrypt(&ctx, out, in);

    for (size_t i = 0; i < 16; i++) printf("%d ", out[i]);
    printf("\n");

    return 0;
}
